void Int_oled_init()
{
    uint8_t buffer[8];

    // 关闭显示屏
    buffer[0] = 0xAE;

    // 设置充电泵  7.5V
    buffer[1] = 0x8D;
    buffer[2] = 0X14;

    // 设置行重映射
    buffer[3] = 0xC8;

    // 设置列重映射
    buffer[4] = 0xA1;

    // 设置寻址模式（水平寻址）
    buffer[5] = 0x20;
    buffer[6] = 0x00;

    // 开启显示屏
    buffer[7] = 0xAF;

    Int_oled_sendDataBytes(0, buffer, 7);
    Int_oled_clear(); // 先清屏再开启，避免出现雪花
    Int_oled_sendDataBytes(0, buffer + 7, 1);
}

void Int_oled_clear()
{
    //memset(bytes_temp,0, 1025);
    Int_oled_setPointer(0,0);
    bytes_temp[0] = 0x40;
    HAL_I2C_Master_Transmit(&hi2c1, 0x78, bytes_temp, 1025, 1000);
}

void Int_oled_sendDataBytes(uint8_t isData,const uint8_t *bytes, uint16_t len)
{
    if (isData)
    {
        bytes_temp[0] = 0x40;
    }
    else
    {
        bytes_temp[0] = 0x00;
    }
    for (uint8_t i = 0; i < len; i++)
    {
        bytes_temp[i + 1] = bytes[i];
    }
    HAL_I2C_Master_Transmit(&hi2c1, 0x78, bytes_temp, len + 1, 1000);
}

void Int_oled_setPointer(uint8_t col, uint8_t row)
{
    uint8_t buffer[6] = {0};
    // 设置column
    buffer[3] = 0x21;
    buffer[4] = col;
    buffer[5] = 127;
    // 设置page
    buffer[0] = 0x22;
    buffer[1] = row;
    buffer[2] = 7;
    Int_oled_sendDataBytes(0, buffer, 6);
}

void Int_oled_displayStr(uint8_t x, uint8_t y, char *str)
{

    uint8_t i = 0;
    Int_oled_setPointer(8 * x, 2 * y);
    // 显示上半部分
    while (str[i] != '\0')
    {
        Int_oled_sendDataBytes(1, Int_oled_Font1[str[i] - 32], 8);
        i++;
    }
    // 显示下半部分
    Int_oled_setPointer(8 * x,2 * y + 1);
    i = 0;
    while (str[i] != '\0')
    {
        Int_oled_sendDataBytes(1, Int_oled_Font1[str[i] - 32] + 8, 8);
        i++;
    }
}

void Int_oled_displayBigNum(uint8_t x, uint8_t y, uint8_t num)
{
    Int_oled_setPointer(8 * x, 2 * y);
    Int_oled_sendDataBytes(1, Int_oled_Font2[num], 16);
    Int_oled_setPointer(8 * x, 2 * y + 1);
    Int_oled_sendDataBytes(1, Int_oled_Font2[num] + 16, 16);
    Int_oled_setPointer(8 * x, 2 * y + 2);
    Int_oled_sendDataBytes(1, Int_oled_Font2[num] + 32, 16);
    Int_oled_setPointer(8 * x, 2 * y + 3);
    Int_oled_sendDataBytes(1, Int_oled_Font2[num] + 48, 16);
}

void Int_oled_displayIcon(uint8_t x, uint8_t y, uint8_t num)
{
    Int_oled_setPointer(8 * x, 2 * y);
    Int_oled_sendDataBytes(1, Int_oled_Font3[num], 8);
    Int_oled_setPointer(8 * x, 2 * y + 1);
    Int_oled_sendDataBytes(1, Int_oled_Font3[num] + 8, 8);
}

void Int_oled_On(void)
{ 
    uint8_t buffer = 0xAF;
    Int_oled_sendDataBytes(0, &buffer, 1);
}

void Int_oled_Off(void)
{
    uint8_t buffer = 0xAE;
    Int_oled_sendDataBytes(0, &buffer, 1);
}
